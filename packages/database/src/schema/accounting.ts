/**
 * عداد أرقام الفواتير — Invoice Counters
 * يحل محل subcollection في Firestore
 * نستخدم PostgreSQL sequence + advisory lock بدلاً من transaction conflict
 */
export const invoiceCounters = pgTable(
  'invoice_counters',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    agencyId: uuid('agency_id')
      .notNull()
      .references(() => agencies.id, { onDelete: 'cascade' }),

    invoiceType: invoiceTypeEnum('invoice_type').notNull(),
    year: integer('year').notNull(),

    // الرقم الحالي — يُحدَّث بـ UPDATE ... RETURNING داخل transaction
    currentSequence: integer('current_sequence').notNull().default(0),

    updatedAt: timestamp('updated_at', { withTimezone: true })
      .notNull()
      .defaultNow(),
  },
  (t) => [
    unique('ic_agency_type_year_unique').on(t.agencyId, t.invoiceType, t.year),
    index('ic_agency_id_idx').on(t.agencyId),
  ]
);
